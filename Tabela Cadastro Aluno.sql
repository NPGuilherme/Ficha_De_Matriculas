/* HAVING - UPDATE */

--- Buscar as turmas com mais de 5 alunos?

SELECT TURMA_ALUNO, COUNT(*) AS 'TOTAL ALUNOS'
FROM CADASTRO_ALUNO
GROUP BY TURMA_ALUNO
HAVING COUNT(*) > 6;

