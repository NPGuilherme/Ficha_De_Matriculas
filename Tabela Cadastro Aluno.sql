/* CALCULAR DIAS DA SEMANA */


--- Data e Horas do Dia Atual
SELECT GETDATE();

---- Data do Dia Anterior e Horas do Dia Atual
SELECT DATEADD(D,-1,GETDATE()) AS 'ONTEM';

--- Data do dia anterior
SELECT CAST(DATEADD(D,-1,GETDATE())AS DATE) AS 'DATA_DE_ONTEM';

--- Data do Dia Atual
SELECT CAST(GETDATE()AS DATE)AS 'DATA_DE_HOJE';

--- Quantidade de Semana do Início do Ano até Hoje
SELECT DATEPART(WEEK,GETDATE()) AS 'QUANTIDADE_DE_SEMANAS';

--- Quantidade de Semana do Início do Ano até Mês Passado
SELECT DATEPART(WEEK,GETDATE()) - 1 AS 'QUANTIDADE_DE_SEMANAS';