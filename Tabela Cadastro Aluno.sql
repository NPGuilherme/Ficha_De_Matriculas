/* COUNT() - GROUP BY - ORDER BY - DESC - ASC  */

--- Quantos alunos existem no total?

SELECT COUNT(*) AS 'QUANTIDADE DE ALUNOS'
FROM CADASTRO_ALUNO;

--- Quero Nome, Sexo e Turma dos alunos na ordem decrescente. 

SELECT NOME_ALUNO, SEXO, TURMA_ALUNO
FROM CADASTRO_ALUNO
ORDER BY NOME_ALUNO DESC;

--- Quantos alunos do sexo masculino e feminino?

SELECT SEXO, COUNT(*) AS 'TOTAL'
FROM CADASTRO_ALUNO
GROUP BY SEXO;


--- Quais turmas existente?

SELECT TURMA_ALUNO AS 'TURMA EXISTENTE' 
FROM CADASTRO_ALUNO
GROUP BY TURMA_ALUNO;


--- Quantos alunos existem em cada turma? 

SELECT  TURMA_ALUNO, COUNT(*) AS 'TOTAL'
FROM CADASTRO_ALUNO
GROUP BY TURMA_ALUNO;


--- Qual turma tem mais alunos? (Decrescente DESC Z/A)

SELECT TOP 1 TURMA_ALUNO, COUNT(IDALUNO) AS 'QUANTIDADE DE ALUNOS'
FROM CADASTRO_ALUNO
GROUP BY TURMA_ALUNO 
ORDER BY 'QUANTIDADE DE ALUNOS' DESC;


--- Qual turma tem menos alunos?

SELECT TOP 1 TURMA_ALUNO, COUNT(IDALUNO) AS "TOTAL MENOR"
FROM CADASTRO_ALUNO
GROUP BY TURMA_ALUNO
ORDER BY [TOTAL MENOR] ASC;


--- Quero nome e Id dos alunos na ordem (Crescente ASC POR NOME - A/Z)

SELECT NOME_ALUNO,IDALUNO 
