/*Operados = <> - < - > LIKE AND - OR - IN - BETWEEN */

--- Nome dos alunos da turma 1A

SELECT NOME_ALUNO 
FROM CADASTRO_ALUNO
WHERE TURMA_ALUNO = '1A';


--- Quais os Alunos da turma 1B?

SELECT NOME_ALUNO, TURMA_ALUNO
FROM CADASTRO_ALUNO
WHERE TURMA_ALUNO = '1B';


--- Quais alunos nascidos após 2023?

SELECT NOME_ALUNO, NASCIMENTO
FROM CADASTRO_ALUNO
WHERE NASCIMENTO > '2023';


--- Quais alunos começam com a letra C?

SELECT IDALUNO, NOME_ALUNO 
FROM CADASTRO_ALUNO
WHERE NOME_ALUNO LIKE 'C%';


--- Quais alunos que terminam com a Letra 'A'

SELECT IDALUNO, NOME_ALUNO 
FROM CADASTRO_ALUNO
WHERE NOME_ALUNO LIKE '%A';


--- Buscar os alunos do sexo masculino da turma '1A' - Operador  AND

SELECT NOME_ALUNO, SEXO, TURMA_ALUNO
FROM CADASTRO_ALUNO
WHERE SEXO = 'MASCULINO' AND TURMA_ALUNO = '1A';

--- Bucar os alunos do sexo masculino ou feminino da turma '1B' - Operadores  OR e AND

SELECT NOME_ALUNO , SEXO, TURMA_ALUNO
FROM CADASTRO_ALUNO
WHERE (SEXO = 'Masculino' OR SEXO = 'Feminino') AND  TURMA_ALUNO = '1B';


--- Buscar os alunos onde o sexo seja diferente de Masculino - Operador <>

SELECT NOME_ALUNO, SEXO, TURMA_ALUNO
FROM CADASTRO_ALUNO
WHERE SEXO <> 'Masculino';

--- Buscar os alunos onde o sexo seja diferente de feminino - Operador <>

SELECT NOME_ALUNO, SEXO, TURMA_ALUNO
FROM CADASTRO_ALUNO
WHERE SEXO <> 'F';


--- Quantidade de alunos masculino e feminino - Operador (IN)

SELECT SEXO, COUNT(*) AS 'TOTAL ALUNOS'
FROM CADASTRO_ALUNO
WHERE SEXO IN ('Masculino','Feminino')
GROUP BY SEXO;

--- Busca as matriculas 002, 008 e 016 - Operador (IN)

SELECT NOME_ALUNO, SEXO, MATRICULA
FROM CADASTRO_ALUNO
WHERE MATRICULA IN (002,016,008);



--- Between buscar os alunos que nasceram entre 01-01-2022 até 30-06-2023

SELECT NOME_ALUNO , NASCIMENTO
FROM CADASTRO_ALUNO
WHERE NASCIMENTO BETWEEN '2022-01-01' AND '2023-06-30'
ORDER BY NASCIMENTO;





/* DATEDIFF - GETDATE */

--- Calcular idade dos alunos:

SELECT NOME_ALUNO,
	DATEDIFF(YEAR, NASCIMENTO, GETDATE()) AS "IDADE"
FROM CADASTRO_ALUNO;


--- Quais alunos são mais velhos?

SELECT  NOME_ALUNO, DATEDIFF(YEAR, NASCIMENTO, GETDATE()) AS IDADE
FROM CADASTRO_ALUNO
WHERE DATEDIFF(YEAR, NASCIMENTO, GETDATE()) = 6;


--- Quero saber quantos dias tem do nascimento até a data atual

SELECT 
    NOME_ALUNO, 
    NASCIMENTO, 
    DATEDIFF(DAY, NASCIMENTO, GETDATE()) AS 'DIAS'
FROM CADASTRO_ALUNO
WHERE DATEDIFF(DAY, NASCIMENTO, GETDATE()) <= 10000;



/* TOP PERCENT */

--- Quais são os primeiros 5 alunos cadastrados?

SELECT TOP 5 IDALUNO, NOME_ALUNO 
FROM CADASTRO_ALUNO;


