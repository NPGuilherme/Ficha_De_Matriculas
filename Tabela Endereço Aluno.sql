--- Estrutura para criar a Tabela Endereço Aluno

CREATE TABLE ENDERECO_ALUNO(
    IDENDERECO INT IDENTITY PRIMARY KEY,
    RUA VARCHAR(30) NOT NULL,
    BAIRRO VARCHAR(20) NOT NULL,
    CIDADE VARCHAR(15),
    UF CHAR(2),
    ID_ALUNO INT
);


/* FOREIGN KEY */

--- Comando para inserir Foreign Key (Chave Estrangeira) na tabela Endereço Aluno referente a Tabela Cadastro Aluno

ALTER TABLE ENDERECO_ALUNO ADD CONSTRAINT FK_ENDERECO_ALUNO
FOREIGN KEY(ID_ALUNO) REFERENCES CADASTRO_ALUNO(IDALUNO);


