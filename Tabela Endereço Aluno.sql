--- Inner Join usando 3 tabelas
--- Buscando Nome e Turma do aluno, Nome Pai, Nome Mãe, Rua e Bairro 


SELECT  A.NOME_ALUNO, A.TURMA_ALUNO,
        R.NOME_PAI, R.NOME_MAE,
        E.RUA, E.BAIRRO
FROM CADASTRO_ALUNO A
INNER JOIN CADASTRO_RESPONSAVEL R
ON A.IDALUNO = R.ID_ALUNO
INNER JOIN ENDERECO_ALUNO E
ON A.IDALUNO = E.ID_ALUNO;


--- Quero todos alunos que moram no (Centro), nome da rua e bairro

SELECT C.IDALUNO, C.NOME_ALUNO,
        E.RUA, E.BAIRRO
FROM CADASTRO_ALUNO C
INNER JOIN ENDERECO_ALUNO E
ON C.IDALUNO = E.ID_ALUNO
WHERE BAIRRO = 'CENTRO';


--- Quero Nome Pai, Nome Mãe, Rua, Bairro, Cidade

SELECT R.IDRESPONSAVEL, R.NOME_PAI, R.NOME_MAE,
        E.RUA, E.BAIRRO, E.CIDADE
FROM CADASTRO_RESPONSAVEL R
INNER JOIN ENDERECO_ALUNO E
ON R.IDRESPONSAVEL = E.IDENDERECO;


--- Quantos alunos moram em cada bairro?
 
SELECT BAIRRO, COUNT(*) AS 'QUANTIDADE'
FROM ENDERECO_ALUNO
GROUP BY BAIRRO;
