    IDTELEFONE INT IDENTITY PRIMARY KEY,
    TIPO CHAR(3) CHECK (TIPO IN ('RES','COM','CEL')) NULL,
    NUMERO VARCHAR (15) NULL,
    ID_ALUNO INT
);


