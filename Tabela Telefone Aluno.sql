/* LEFT JOIN */

--- Comando busca Nome, tipo de telefone e número
--- A função Isnull verifica se uma expressão é nula e a substitui por um valor alternativo, facilitando a leitura de relatórios
--- Left Join retorna todos os registro da tabela da esquerda (Primeira Tabela), e os registros correspondentes da tabela direita
--- é usado para manter discrição da tabela principal mesmo sem dados relacionados


SELECT  A.IDALUNO, A.NOME_ALUNO,
        ISNULL(T.TIPO, 'SEM') AS "TIPO", 
        ISNULL(T.NUMERO,'NUMERO') AS"NUMERO"
FROM CADASTRO_ALUNO A
LEFT JOIN TELEFONE_ALUNO T
ON A.IDALUNO = T.ID_ALUNO;

--- Right Join retorna todos os registros das tabelas da direita, depois do (Right join)
SELECT  A.IDALUNO, A.NOME_ALUNO,
        ISNULL(T.TIPO, 'SEM') AS "TIPO", 
        ISNULL(T.NUMERO,'NUMERO') AS"NUMERO"
FROM CADASTRO_ALUNO A
RIGHT JOIN TELEFONE_ALUNO T
ON A.IDALUNO = T.ID_ALUNO;


--- Quero Nome Aluno, Tipo de Telefone e Número

SELECT	C.NOME_ALUNO,
		T.TIPO, T.NUMERO
FROM CADASTRO_ALUNO C
INNER JOIN TELEFONE_ALUNO T
ON C.IDALUNO = T.ID_ALUNO;


/* OPERADOR LIKE */

--- Comando busca o tipo e número de telefone
--- Like '[52]%' traz os números que começam com o números 5 ou 2

SELECT TIPO, NUMERO
FROM TELEFONE_ALUNO
WHERE NUMERO LIKE '[52]%';


--- Comando busca o Tipo e número de telefone
--- Like '[^52]%' os números que NÃO começam com os números 5 e 2

SELECT TIPO, NUMERO
FROM TELEFONE_ALUNO
WHERE NUMERO LIKE '[^52]%';