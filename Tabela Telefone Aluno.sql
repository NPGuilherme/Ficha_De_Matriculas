--- Comando para inserir Foreign Key (Chave Estrangeira) na tabela Telefone Aluno referente a Tabela Cadastro Aluno

ALTER TABLE TELEFONE_ALUNO ADD CONSTRAINT FK_TELEFONE_ALUNO
FOREIGN KEY(ID_ALUNO) REFERENCES CADASTRO_ALUNO(IDALUNO);

