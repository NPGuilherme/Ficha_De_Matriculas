FROM CADASTRO_RESPONSAVEL
WHERE IDRESPONSAVEL = 10;


/* SUBCONSULTA */

--- SubConsulta - Busca o Nome do Pai dos Alunos da Turma '1A'

SELECT NOME_PAI
FROM CADASTRO_RESPONSAVEL
WHERE ID_ALUNO IN (
    SELECT IDALUNO
    FROM CADASTRO_ALUNO
    WHERE TURMA_ALUNO = '1A'
);

