/* INNER JOIN */

--- Comando Inner Join busca registro de duas ou mais tabelas
--- Comando para concatenar (juntar) dois ou mais registro em um só campo

SELECT  NOME_ALUNO+ ' : '+CPF AS "NOME - CPF",
        NOME_PAI+ ' : ' +CPF_PAI AS "NOME - CPF",
        NOME_MAE+ ' : ' +CPF_MAE AS "NOME - CPF"
FROM CADASTRO_ALUNO
INNER JOIN CADASTRO_RESPONSAVEL
ON IDALUNO = ID_ALUNO;

SELECT 	C.NOME_ALUNO,
		R.EMAIL_PAI, R.EMAIL_MAE
FROM CADASTRO_ALUNO C
INNER JOIN CADASTRO_RESPONSAVEL R
ON C.IDALUNO = R.IDRESPONSAVEL;


--- Quero apenas o nome e cpf do pai, Id do aluno (10)

