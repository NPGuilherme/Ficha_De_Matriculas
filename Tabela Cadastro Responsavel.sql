
--- Depois de criar a tabela de aluno, criei uma segunda tabela para armazenar as informações dos responsalveis.
--- Criei uma chave estrangeira para relacionar cada responsavel a um aluno.


/* CREATE TABLE */

--- Estrutura para Criar a Tabela Cadastro Responsavel

CREATE TABLE CADASTRO_RESPONSAVEL(
    IDRESPONSAVEL INT IDENTITY(1,1) PRIMARY KEY,
    NOME_PAI VARCHAR(40)NOT NULL,
    CPF_PAI VARCHAR(15) UNIQUE,
    EMAIL_PAI VARCHAR(30)NULL,
    NOME_MAE VARCHAR(40)NOT NULL,
    CPF_MAE VARCHAR(15) UNIQUE, 
    EMAIL_MAE VARCHAR(30)NULL,
    ID_ALUNO INT
);


/* FOREIGN KEY */

--- Comando para inserir Foreign Key (Chave Estrangeira) na tabela Cadastro Responsavel referente a Tabela Cadastro Aluno

ALTER TABLE CADASTRO_RESPONSAVEL ADD CONSTRAINT FK_RESPONSAVEL_ALUNO
FOREIGN KEY (ID_ALUNO) REFERENCES CADASTRO_ALUNO(IDALUNO);


