/* CREATE TABLE */

--- Estrutura para criar a Tabela Funcionario

CREATE TABLE PROFESSORES(
    IDPROFESSORA INT IDENTITY(1,1) PRIMARY KEY,
    NOME_PROFESSORA VARCHAR(30) NOT NULL,
    TURMA_PROFESSORA VARCHAR(20),
    SALARIO MONEY
);


