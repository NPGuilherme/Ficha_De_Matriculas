/* OPERADOR IN */

--- Quero nome e turma das professoras com ID (1) e (3)

SELECT IDPROFESSORA, NOME_PROFESSORA, TURMA_PROFESSORA
FROM PROFESSORES
WHERE IDPROFESSORA IN ('1','3');


/* INNER JOIN */

--- Quero quantidade de alunos e as turmas dos professores de ID (2) e (4)  

SELECT COUNT (*)AS 'QUANTIDADE_ALUNO',
F.IDPROFESSORA, F.NOME_PROFESSORA+ ' --- TURMA --- ' + TURMA_PROFESSORA AS "PROFESSORAS"
FROM CADASTRO_ALUNO A
INNER JOIN PROFESSORES F
ON A.TURMA_ALUNO = F.TURMA_PROFESSORA
WHERE IDPROFESSORA IN ('2','4')
GROUP BY A.TURMA_ALUNO ,F.IDPROFESSORA,F.NOME_PROFESSORA,F.TURMA_PROFESSORA;


--- Quero quantidade de alunos e turmas de todas professoras

SELECT COUNT (*)AS 'QUANTIDADE_ALUNO',
F.NOME_PROFESSORA+ ' --- TURMA -- ' + TURMA_PROFESSORA AS "PROFESSORAS"
FROM CADASTRO_ALUNO A
INNER JOIN PROFESSORES F
ON A.TURMA_ALUNO = F.TURMA_PROFESSORA
GROUP BY A.TURMA_ALUNO ,F.NOME_PROFESSORA,TURMA_PROFESSORA;




--- Quero nome dos alunos, nome das professoras e turmas

SELECT  A.NOME_ALUNO,
F.NOME_PROFESSORA, F.TURMA_PROFESSORA
FROM CADASTRO_ALUNO A
INNER JOIN PROFESSORES F
ON A.TURMA_ALUNO = F.TURMA_PROFESSORA
ORDER BY TURMA_PROFESSORA;


/* CAST - CONVERT */

--- Simulação de aumento de 20% do salário
--- Cast é uma função usada para converter um valor
--- Salario * 1.20. Conta para aumentar 20% do salário
--- AS Decimal(10,2). garante o formato com 2 casas decimais, de moeda. 

SELECT NOME_PROFESSORA, 
       SALARIO,
       CAST(SALARIO * 1.20 AS DECIMAL(10, 2)) AS 'NOVO SALARIO'
FROM PROFESSORES;


--- Simulação de aumento de salário de 30% com a função CONVERT

SELECT NOME_PROFESSORA, 
       SALARIO,
       CONVERT(DECIMAL(10, 2), SALARIO * 1.30) AS 'NOVO SALARIO'
FROM PROFESSORES;


