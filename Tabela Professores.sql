/* OPERADOR < - >= - > */

--- Buscar o Funcionário com Salário Menor (<) que 4500

SELECT IDPROFESSORA, NOME_PROFESSORA, SALARIO
FROM PROFESSORES
WHERE SALARIO < 4500;

--- Buscar Funcionário com Salário Menor ou Igual (<=) que 4510

SELECT IDPROFESSORA, NOME_PROFESSORA, SALARIO
FROM PROFESSORES
WHERE SALARIO >= 4510;


--- Buscar o Funcionário com Salário Maior (>) que 4510

SELECT IDPROFESSORA, NOME_PROFESSORA, SALARIO
FROM PROFESSORES
WHERE SALARIO > 4510;


/* FUNÇÃO DE AGREGAÇÃO  MAX - MIN - AVG - SUM */

--- Qual o maior salário?
SELECT MAX(SALARIO) AS 'MAIOR_SALARIO' FROM PROFESSORES;

--- Qual o menor salário?
SELECT MIN(SALARIO) AS 'MENOR_SALARIO' FROM PROFESSORES;

--- Qual a media do total de salário
SELECT AVG(SALARIO) AS 'MEDIA_SALARIO' FROM PROFESSORES;

--- Qual a soma de todos os salários?
SELECT SUM(SALARIO) AS 'SOMA_SALARIOS' FROM PROFESSORES;


